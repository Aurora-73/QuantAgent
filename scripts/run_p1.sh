#!/bin/bash
# ==============================================================================
# P1 任务运行脚本 - 服务器版
# 
# 功能：运行完整的 P1 闭环验证流程
# 包含：数据更新 → 因子计算 → 因子评估 → 压力测试 → Brinson归因 → 日报
# 
# 使用方式：
#   bash scripts/run_p1.sh                  # 运行全部任务
#   bash scripts/run_p1.sh --update-only    # 仅更新数据
#   bash scripts/run_p1.sh --date 2026-07-06 # 指定日期
# ==============================================================================

set -e

PROJECT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$PROJECT_DIR"

# ========================================
# 参数解析
# ========================================
TARGET_DATE=""
UPDATE_ONLY=false

while [[ $# -gt 0 ]]; do
    case $1 in
        --date)
            TARGET_DATE="$2"
            shift 2
            ;;
        --update-only)
            UPDATE_ONLY=true
            shift
            ;;
        *)
            echo "未知参数: $1"
            exit 1
            ;;
    esac
done

# ========================================
# 环境配置
# ========================================
echo "=============================================="
echo "  P1 任务运行脚本"
echo "  项目目录: $PROJECT_DIR"
echo "=============================================="

# 激活虚拟环境
if [[ -f ".venv/bin/activate" ]]; then
    echo "激活虚拟环境..."
    source .venv/bin/activate
fi

# ========================================
# 数据更新（国内数据源直连，不走代理）
# ========================================
echo ""
echo "=============================================="
echo "[1/6] 数据更新 (国内直连)"
echo "=============================================="

# 清除代理（国内数据源）
unset http_proxy https_proxy HTTP_PROXY HTTPS_PROXY

if [[ -n "$TARGET_DATE" ]]; then
    echo "日期: $TARGET_DATE"
    python -m scripts.update_data --universe csi300 --start "$TARGET_DATE"
else
    python -m scripts.update_data --universe csi300
fi

# ========================================
# 因子批量计算
# ========================================
if [[ "$UPDATE_ONLY" == false ]]; then
    echo ""
    echo "=============================================="
    echo "[2/6] 因子批量计算"
    echo "=============================================="
    python -m scripts.compute_factors --universe csi300

    # ========================================
    # 因子评估 + 衰减检测
    # ========================================
    echo ""
    echo "=============================================="
    echo "[3/6] 因子评估 + 衰减检测"
    echo "=============================================="
    python -m scripts.evaluate_factors

    # ========================================
    # 压力测试
    # ========================================
    echo ""
    echo "=============================================="
    echo "[4/6] 压力测试"
    echo "=============================================="
    python -m scripts.run_stress_test --universe csi300

    # ========================================
    # Brinson 归因
    # ========================================
    echo ""
    echo "=============================================="
    echo "[5/6] Brinson 归因"
    echo "=============================================="
    python -m scripts.run_brinson_attribution

    # ========================================
    # Daily Research（完整流程）
    # ========================================
    echo ""
    echo "=============================================="
    echo "[6/6] Daily Research（完整流程）"
    echo "=============================================="
    if [[ -n "$TARGET_DATE" ]]; then
        python -m scripts.daily_research --date "$TARGET_DATE"
    else
        python -m scripts.daily_research
    fi
fi

# ========================================
# 最终验证
# ========================================
echo ""
echo "=============================================="
echo "[验证] 数据库统计"
echo "=============================================="
python -m scripts.db_stats

echo ""
echo "=============================================="
echo "✅ P1 任务完成!"
echo "=============================================="